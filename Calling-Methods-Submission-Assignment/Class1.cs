using System;

// Define a class named 'Operations' to perform various math functions
public class Operations
{
    // Method to double the given number
    public int Double(int number)
    {
        return 2 * number; // Multiplies the input by 2
    }

    // Method to square the given number
    public int Square(int number)
    {
        return number * number; // Multiplies the input by itself
    }

    // Method to increment the given number by 10
    public int AddTen(int number)
    {
        return number + 10; // Adds 10 to the input
    }
}
