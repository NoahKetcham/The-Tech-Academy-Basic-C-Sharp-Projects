using System;

public class Operations
{
    // Method to double the number
    public int Double(int number)
    {
        return 2 * number;
    }

    // Method to square the number
    public int Square(int number)
    {
        return number * number; 
    }

    // Method to increment the number by 10
    public int AddTen(int number)
    {
        return number + 10;
    }
}
